async function main() {
  // Find admin user
  const admin = await db.user.findUnique({ where: { mobile: ADMIN_MOBILE } });
  if (!admin) {
    console.error("Admin user not found. Make sure 09300083803 exists.");
    process.exit(1);
  }

  let created = 0;
  let skipped = 0;

  for (const art of articles) {
    const existing = await db.article.findUnique({ where: { slug: art.slug } });
    if (existing) {
      // Update existing
      await db.article.update({
        where: { id: existing.id },
        data: {
          title: art.title,
          excerpt: art.excerpt,
          content: art.content,
          category: art.category,
          tags: art.tags,
          coverImage: art.coverImage,
          status: "published",
          seoTitle: art.seoTitle,
          seoDescription: art.seoDescription,
          metaKeywords: art.metaKeywords,
          robots: art.robots,
          authorId: admin.id,
        },
      });
      console.log(`Updated: ${art.slug}`);
      skipped++;
    } else {
      // Create new
      // Estimate reading time
      const wordCount = art.content.trim().split(/\s+/).length;
      const readingMinutes = Math.max(1, Math.ceil(wordCount / 200));
      await db.article.create({
        data: {
          title: art.title,
          slug: art.slug,
          excerpt: art.excerpt,
          content: art.content,
          category: art.category,
          tags: art.tags,
          coverImage: art.coverImage,
          status: "published",
          seoTitle: art.seoTitle,
          seoDescription: art.seoDescription,
          metaKeywords: art.metaKeywords,
          robots: art.robots,
          readingMinutes,
          authorId: admin.id,
        },
      });
      console.log(`Created: ${art.slug} (${wordCount} words, ${readingMinutes} min read)`);
      created++;
    }
  }

  console.log(`\nDone! Created: ${created}, Updated: ${skipped}`);
}

